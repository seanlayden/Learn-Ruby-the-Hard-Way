puts - prints output with a line break built in.
print - prints output without a line break.
# - used for commenting lines
#{} - used to call a variable inside of a string.
" vs ' - Double quotes are used for string interpolation
%{} vs #{} - %{} allows for multiple objects to be manipulated at once.
""" - helps us use quotation marks inside of a string.
\ - the escape character.
ARGV - the argument variable. Holds the arguments that are passed to a ruby script when it is run.
